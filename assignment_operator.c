
#include <stdio.h>
int main()
{
 //---equal to---
 int num = 5;
 printf("%d\n", num);
 
 //---plus equal to---
 int num1 = 5;
 num1 += 1;  //num1 = (num1 + 1);
 printf("%d\n", num1);
 
 //---minus equal to---
  int num2 = 5;
 num2 -= 1;   //num2 = (num2 - 1);
 printf("%d\n", num2);
 
 //---multiply equal to---
  int num3 = 5;
 num3 *= 2;  //num3 = (num3 * 2);
 printf("%d\n", num3);
 
 //---divide equal to---
  int num4 = 8;
 num4 /= 2;    //num4 = (num4 / 2);
 printf("%d\n", num4);
 
 //---modulas equal to---
  int num5 = 8;
 num5 %= 3;     //num5 = (num5 % 2);
 printf("%d\n", num5);
}
